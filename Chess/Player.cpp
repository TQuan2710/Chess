#include "Player.h"
/*-----------------------------------------------------------------------------------*/
Player::Player()
{
	name = "";
	numberOfLose = 0;
	numberOfWin = 0;
	movePiece = "";
}
/*-----------------------------------------------------------------------------------*/
Player::~Player()
{

}
/*-----------------------------------------------------------------------------------*/
Player::Player(std::string s)
{
	name = s;
}
/*-----------------------------------------------------------------------------------*/
std::string Player::GetName()
{
	return name;
/*-----------------------------------------------------------------------------------*/
}
void Player::addMovePiece(std::string move)
{
	movePiece += move;
}
/*-----------------------------------------------------------------------------------*/
void Player::backMovePiece()
{
	int i = 4;
	while (i--) movePiece.pop_back();
}
